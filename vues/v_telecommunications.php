    </p>
    <p>
        Dans tous les espaces, un réseau Wifi "visiteurs" est disponible, avec une clé WPA renouvelée régulièrement et
        communiquée aux ligues. Ce réseau ne permet que l'accès à Internet.
    </p>

    <p class ="lead">
        Téléphonie
    </p>
    <p>
        Dans les bâtiments anciens A et B, les salles et bureaux sont équipés de prises de téléphone analogiques. La M2L
        y fournit les combinés téléphoniques. Dans les bâtiments neufs C et D, l'équipement téléphonique est de type
        VoIP. La M2L loue des postes téléphoniques IP aux ligues.
    </p>
