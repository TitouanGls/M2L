    <h4 class="mt-5">Présentation des services Internets</h4>

    <br/>
    <br/>
    <p class ="lead">
        Reseaux Informatique
    </p>
    <p>
        La M2L dispose actuellement
        :
        <br/>
        d'un réseau commun aux ligues et à l'administration,
        <br/>
        d'un   réseau   de   type   DMZ   dans   lequel   on   trouve   un   ensemble   de   serveurs
        accessibles depuis l'extérieur,
        <br/>
        d'une connexion à internet.
        Note : une segmentation des réseaux est en projet pour augmenter la sécurité

    </p>
    <p class ="lead">
        Serveur FTP documentaire
    </p>
    <p>
        La M2L met à disposition  des ligues un serveur FTP documentaire  intranet/internet
        regroupant des textes légaux, des modèles de dossiers, de statuts, des programmes de
        formation (...) compilés par le CROSL.
    </p>

    <p class ="lead">
        Système de gestion des configurations
    </p>
    <p>
        M2L   gère   à   travers   un   logiciel   de   gestion   des   configurations   l'ensemble   du   parc
        informatique incluant les postes fixes des ligues.
    </p>

    <p class ="lead">
        Intégration des postes informatiques des ligues
    </p>
    <p>Lorsque les ligues acquièrent du matériel informatique, il y a une phase obligatoire
        d'intégration qui consiste à
        :
        <br/>
        installer un antivirus affilié au serveur antiviral de la M2L,
        <br/>
        installer la dernière version de l'agent qui réalise l’inventaire matériel et logiciel
        <br/>
        paramétrer le poste en adressage IP automatique,
        <br/>
        installer un système de sauvegarde de données sur un site FTP de sauvegarde
        géré par la M2L,
        <br/>
        effectuer les dernières mises à jour systèmes et à paramétrer leur automatisation,
        <br/>
        paramétrer les noms des postes selon les règles de gestion suivantes
        :
        <br/>
        B[code bâtiment]E[numéro étage]L[numéro ligue]S[numéro salle].P[numéro poste]
        Code bâtiment
        qui peut être A ou C
        N° étage
        est compris  entre  1 et 4 (puisque  les locaux  du  rez-de-chaussée
        n'hébergent pas de ligues)
        N° ligue
        sur 2 chiffres : correspond à un nombre attribué à la ligue allant pour
        l'instant de 01 à 24
        N° salle
        sur 2 chiffres
        : correspond aux bureaux occupés par les ligues
        N° poste
        sur 2 chiffres
        : correspond au numéro écrit sur la prise murale
        Exemple
        : le nom d'hôte
        BAE2L06S01P01
        correspond au poste installé sur la
        prise N°1 du bureau A201 occupé par la ligue de Volley, bureau situé au deuxième
        étage du bâtiment A.
        Cette   intégration   est   contractualisée.   Les   ligues   et   CD   étant   toutes   des   structures
        associatives indépendantes, leurs postes ne sont pas intégrés dans un annuaire central.
        Par contre, les postes de l'administration de la M2L et de la salle multimédia le sont
    </p>

